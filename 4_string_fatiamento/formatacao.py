curso = " pYTHon    "

print(curso.upper()) # converte todos os caracteres para maiúsculo

print(curso.lower()) # converte todos os caracteres para minúsculo

print(curso.title()) # converte todos os caracteres para minúsculo exceto o primeiro caractere

print(curso.strip()) # elimina todos os espaços em branco inseridos pelo usuário

print(curso.lstrip()) # elimina todos os espaços em branco á esquerda

print(curso.rstrip()) # elimina todos os espaços em branco á direita

print(curso.center(10, "#")) # força a string a ter o número de caracteres determinado(se tiver faltando insere com o caractere que definirmos) e deixa centralizado os caracteres principais da string

print(".".join(curso)) # insere o caractere definido em cada intervalo de exibição da string