nome = "Murilo Santana de Souza"

print(nome[0]) # exibe oque está contido no primeiro caractere, ou no que definirmos

print(nome[-3]) # puxa o caractere da direita para a esquerda

print(nome[:6]) # exibe a quantidade de caracteres desejada

print(nome[7:]) # exibe os caracteres desejados após a casa definida

print(nome[7:14]) # exibe os caracteres desejados dentro de um intervalo

print(nome[7:14:2]) # último parâmetro puxa por exemplo a string de 2 em 2

print(nome[:]) # sem parâmetro exibe toda a string

print(nome[::-1]) # espelhamento da string, exibe ao contrário